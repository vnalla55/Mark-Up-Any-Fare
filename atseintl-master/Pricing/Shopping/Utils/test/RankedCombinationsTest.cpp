class RankedCombinationsTest: public Test
{
public:
  void SetUp(){}
  void TearDown(){}
  std::shared_ptr<RankedCombinations> _rankedCombinations;

  void createRC(size_t width)
  {
    _rankedCombinations.reset(new RankedCombinations(width));
  }

  void assureExhausted()
  {
    for (size_t i = 0; i < EXHAUSTED_CHECKS_COUNT; ++i)
    {
      ASSERT_EQ(RankedCombinationsIndices(),
          _rankedCombinations->next());
    }
  }

  template <int N, int M>
  void validate(const size_t array[N][M], bool assertExhausted=false)
  {
    for (size_t i = 0; i < N; ++i)
    {
      RankedCombinationsIndices fromArray;
      for(size_t j = 0; j < M; ++j)
      {
        fromArray.push_back(array[i][j]);
      }

      RankedCombinationsIndices fromGenerator = _rankedCombinations->next();
      ASSERT_EQ(fromArray, fromGenerator);
    }
    if (assertExhausted)
    {
      assureExhausted();
    }
  }

  static size_t EXHAUSTED_CHECKS_COUNT;

  static size_t ONE_DIM[200][1];
  static size_t TWO_DIM[200][2];
  static size_t THREE_DIM[200][3];
  static size_t FOUR_DIM[200][4];
  static size_t FIVE_DIM[200][5];
  static size_t SIX_DIM[200][6];
  static size_t SEVEN_DIM[200][7];

  static size_t ONE_DIM_BOUNDED_6[][1];
  static size_t TWO_DIM_BOUNDED_3_7[][2];
  static size_t THREE_DIM_BOUNDED_3_5_2[][3];
  static size_t FOUR_DIM_BOUNDED_4_3_1_3[][4];
  static size_t FIVE_DIM_BOUNDED_3_2_2_5_4[][5];
  static size_t SIX_DIM_BOUNDED_2_1_3_5_2_2[][6];
  static size_t SEVEN_DIM_BOUNDED_1_2_3_4_3_2_1[][7];

  static size_t THREE_DIM_FINITE_AND_NOT[][3];
};

